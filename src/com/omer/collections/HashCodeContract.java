package com.omer.collections;

public class HashCodeContract {

    /*
    *
    * x.equals(y) true  --> x.hashCode() == y.hashCode() true
    *
    * x.equals(y) false --> x.hashCode() == y.hashCode() true or false
    *
    * x.hashCode() == y.hashCode() true --> x.equals(y) true or false
    *
    * */
}
